package basicJava;

public class DataTypesEx {

    public static void main(String[] args) {

        char c = 'a';
        int d = c;
        System.out.println(d);

        char c1 ='A';
        int d1 = c1;
        System.out.println(d1);

        char c2 = '0';
        int d2 = c2;
        System.out.println(d2);

        int c3= 'a'-0;
        System.out.println(c3);
    }
}
